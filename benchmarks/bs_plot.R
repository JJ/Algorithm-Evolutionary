bv.base <- read.csv( "bv_bitflip_base.dat.csv", sep=';' )
bs.base <- read.csv( "bs_bitflip_base.dat.csv", sep=';' )
bs.opt <- read.csv( "bs_bitflip_5.10.dat.csv", sep=';' )
bv.opt <- read.csv( "bv_bitflip_5.10.dat.csv", sep=';' )
bs.opt.lm <- lm( bs.opt$Time ~ bs.opt$Bits )
bs.base.lm <- lm( bs.base$Time ~ bs.base$Bits )
bv.base.lm <- lm( bv.base$Time ~ bv.base$Bits )
bv.opt.lm <- lm( bv.opt$Time ~ bv.opt$Bits )

plot( bv.base$Time ~ bv.base$Bits, pch=20, xlab='Bits', ylab='time (secs)', main='Interpreter/representation match' )
lines( bv.base$Bits, fitted(bv.base.lm ), lty='dashed', lwd=3)
points( bv.opt$Time ~ bv.opt$Bits,col='red', pch=23 )
lines( bv.opt$Bits, fitted(bv.opt.lm), col='red', lwd=3)
points( bs.base$Time ~ bs.base$Bits, pch=20,col='blue' )
lines( bs.base$Bits, fitted(bs.base.lm ), col='blue', lty='dashed', lwd=5)
points( bs.opt$Time ~ bs.opt$Bits,col='green', pch=23 )
lines( bs.opt$Bits, fitted(bs.opt.lm), col='green', lwd=5)